# Assign values to variables
a = 10
b = 3


# Arithmetic operations
print(a + b) # Addition
print(a - b) # Subtraction
print(a * b) # Multiplication
print(a / b) # Division
print(a % b) # Modulus
print(a ** b) # Power
print(a // b) # Floor division

#relational operators
a = 10
b = 20


print(a == b)
print(a != b)
print(a < b)

#logical operators:
x = True
y = False


print(x and y)
print(x or y)
print(not x)
